#El siguiente ciclo for imprime los números del 0 al 10, el(11) no se tiene encuenta y aparecen hasta(10)
for i in range(11):
    print(i)

#El siguiente ciclo for imprime los números del 1 al 10f, el(1 antes de la coma es donde inicia y despues de la coma es hasta donde termina, igual no se tiene encuenta el 11)
for j in range (1,11):
    print(j)

#El siguiente ciclo for imprime los números pares del 0 al 10
for k in range(0,11,2): #(0) es donde inicia, (11) es donde termina sin tener en cuetna el 11, quedaria(10), (2) es de cuanto va a ir contando
    print(k)

#El siguiente ciclo for imprime los números pares del 10 al 0
for i in range(10,-1,-2): #(10) es donde inicia, (-1) es donde termina sin tener en cuetna el -1, quedaria(0), (-2) es de cuanto va a ir contando
    print(i)

#El siguiente ciclo for imprime la cadena "Amo programar en python" 10 veces ya que el(11) no se tiene en cuenta
for i in range(11):
    print('Amo programar en python')

#El siguiente ciclo for imprime los cuadrados de los números del 1 al 10
for i in range(1,11):
    print(i**2)
